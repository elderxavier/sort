    private void IniciaTxtLer() {  
        jLabel1.setVisible(true); 
        jTASort.setVisible(false); 
        Thread threadinicia = new Thread() {
            @Override
            public void run(){
                try{
                    DisableButtons();
                    int array[] = ler.converteInt("vetoInteiros.txt");
                    for( int i =0 ; i < array.length; i++){
                        jTALer.append(String.valueOf(array[i])+ "\n");
                    }                    
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(null, ex, "Error:", JOptionPane.ERROR_MESSAGE);
                }
            }

        };
        
        Thread load = new Thread() {  
            public @Override void run() {                  
                if (threadinicia.getState() == Thread.State.NEW) 
                    threadinicia.start(); 
                if (threadinicia.getState() != Thread.State.TERMINATED)
                    try {  
                        threadinicia.join(); 
                    } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex, "Error:", JOptionPane.ERROR_MESSAGE);
                    }                  
                jLabel1.setVisible(false);
                EnableButtons();                
            }  
        };          
        threadinicia.start();
        load.start();
        
    }
    
    
    public void QuickSort() {
        jLabel1.setVisible(true);
        
                
        Thread threadinicia = new Thread() {
            @Override
            public void run(){
                try{
                    DisableButtons();
                    jTASort.setVisible(false); 
                    init = System.currentTimeMillis();  
                    AlgoritimoQuicksort sorter = new AlgoritimoQuicksort(); 
                    int array[] = ler.converteInt("src/arquivos/vetoInteiros.txt");
                    sorter.sort(array);        
                    for(int i:array){        
                        jTASort.append(String.valueOf(i)+ "\n");
                    }                        
                
                jTINVERSOES.setText(String.valueOf(sorter.getInterations()));
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(null, ex, "Error:", JOptionPane.ERROR_MESSAGE);
                }
            }
        };
        
        Thread load = new Thread() {  
            public @Override void run() {                  
                if (threadinicia.getState() == Thread.State.NEW) 
                    threadinicia.start(); 
                if (threadinicia.getState() != Thread.State.TERMINATED)
                    try {  
                        threadinicia.join(); 
                    } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex, "Error:", JOptionPane.ERROR_MESSAGE);
                    }  
                end = System.currentTimeMillis();
                total = end - init;  
                jTTEMPO.setText(""+(total));
                jLabel1.setVisible(false); 
                jTASort.setVisible(true); 
                EnableButtons();                
            }  
        };          
        threadinicia.start();
        load.start();
        
    }
    
